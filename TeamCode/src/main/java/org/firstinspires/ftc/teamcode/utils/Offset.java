package org.firstinspires.ftc.teamcode.utils;

class Offset {
    private final double dx, dy;

    Offset(final double dx, final double dy) {
        this.dx = dx;
        this.dy = dy;
    }

    // Getters
    double getDx() {
        return dx;
    }

    double getDy() {
        return dy;
    }
}
